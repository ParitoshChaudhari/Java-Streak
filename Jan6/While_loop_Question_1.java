package Jan6;

public class While_loop_Question_1 {
    public static void main(String[] args) {
        //CWH JAVA CHAPTER 4 Question 1
        //Write a program to print natural numbers from 100 to 200

        //variable
        int i =100;

        //loop
        while(i<=200){
            System.out.println(i);
            i++;
        }
    }
}
