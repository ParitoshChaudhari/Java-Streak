package Jan6;

public class Do_While_Question_1 {
    public static void main(String[] args) {
        //CWH JAVA CHAPTER 4 Question 2
        //Write a program to print first n natural numbers using a do-while loop

        //Variable
        int i = 1;

        //do-while loop
        do{
            System.out.println(i);
            i++;
        }while(i<=50);

    }
}
