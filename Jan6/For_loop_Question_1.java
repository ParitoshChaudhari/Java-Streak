package Jan6;

public class For_loop_Question_1 {
    public static void main(String[] args) {
        //CWH JAVA CHAPTER 4 Question 3
        //Write a program to print first n natural numbers in reverse order.

        //variable
        int num = 50;

        //loop
        for(int i = num;i>=1;i--){
            System.out.println(i);
        }
    }
}
