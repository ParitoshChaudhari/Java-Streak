package Jan3;


public class Add3Numbers {
    // problem 1 from cwh java course
    // Write a program to sum three numbers in Java.

    public static void main(String[] args) {
        // variables
        int num1 = 52;
        int num2 = 85;
        int num3 = 96;

        // total of 3 numbers
        int total = (num1 + num2 + num3);

        // printing on screen
        System.out.print("Total of 3 numbers is : " + total);
    }

}
