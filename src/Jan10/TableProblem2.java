package Jan10;

public class TableProblem2 {
    public static void main(String[] args) {
        ////CWH JAVA CHAPTER 5 Question 4
        //Write a program to print a multiplication table of 10 in reverse order

        //loop
        for(int i = 10 ; i >= 1 ; i--){
            System.out.println("10 * " + i + " = " + (10*i));
        }
    }
}
